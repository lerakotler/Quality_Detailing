function initMobileTouchScrolling() {
    // –î–ª—è –º–æ–±–∏–ª—å–Ω—ã—Ö —É—Å—Ç—Ä–æ–π—Å—Ç–≤ –¥–æ–±–∞–≤–ª—è–µ–º –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–≥–æ —Å–∫—Ä–æ–ª–ª–∞
    if ('ontouchstart' in window) {
        const blogContainer = document.querySelector('.blog-container');
        if (blogContainer) {
            blogContainer.style.overflowX = 'auto';
            blogContainer.style.webkitOverflowScrolling = 'touch';
        }
        
        // –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ–º –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω—ã–π —Å–∫—Ä–æ–ª–ª –ø—Ä–∏ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–º —Å–∫—Ä–æ–ª–ª–µ
        let startX, startY;
        const blogItems = document.querySelectorAll('.blog-item');
        
        blogItems.forEach(item => {
            item.addEventListener('touchstart', function(e) {
                startX = e.touches[0].pageX;
                startY = e.touches[0].pageY;
            }, { passive: true });
            
            item.addEventListener('touchmove', function(e) {
                if (!startX || !startY) return;
                
                const diffX = e.touches[0].pageX - startX;
                const diffY = e.touches[0].pageY - startY;
                
                // –ï—Å–ª–∏ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω—ã–π —Å–∫—Ä–æ–ª–ª –±–æ–ª—å—à–µ –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–≥–æ, –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ–º –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω—ã–π
                if (Math.abs(diffX) > Math.abs(diffY)) {
                    e.preventDefault();
                }
            }, { passive: false });
        });
    }
}

